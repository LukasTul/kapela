                    <div class="col-lg-8 text-white">
                        <h2 class="text-white mb-4">Jak to vzniklo?</h2>
                        <p>
                        Ahoj, moje jméno je Lukáš. V Neutopii hraju na bicí už několik let a dnes ti povím celou naši backstory. Tak se pohodlně posaď a začínáme!
                        </p> <p>
                        Psal se rok 2019 a já nastupoval na Průmyslovku v Liberci. Na harmonizačním pobytu jsem se seznámil s Filipem. Byli jsme spolu ve stejné třídě. Začali jsme se spolu bavit a když přešla základní témata, dostali jsme se v hovoru k hudbě a zjistili jsme, že jsme do ní oba zapálení. Filip měl za sebou sedm let v základní umělecké škole na housle, avšak nyní tomu byl už rok, co se učil na elektrickou kytaru. Já jsem tolik zkušeností jako on v té době rozhodně neměl, avšak něco jsem už také pochytil. U mě to začalo hraním na cajon někde kolem sedmé třídy, i když bych to mohl datovat až k ťukání na dřívka kolem táboráku. V deváté třídě jsem zjistil, že jeden známý mých rodičů učí v ZUŠ hru na bicí nástroje. Dali jsme se tedy do kontaktu a po společné domluvě jsem k němu přijel na víkend. Celý víkend jsme strávili u bicích a byl to jeden z mých nejlepších zážitků vůbec. Hned jak jsem se vrátil zpět, šel jsem se zaregistrovat do ZUŠ a tak jsem v době, kdy jsme se s Filipem potkali, hrál na bicí asi rok.
                        </p><p>
                        Celý pobyt jsme s Filipem o hudbě mluvili. V té době jsme oba poslouchali převážně Nirvanu a tak bylo o čem mluvit. Když jsme se vrátili, Filip nabídnul, že bychom u něj mohli zkusit zahrát, jelikož má doma bicí. Tak jsme se sešli a začali jsme spolu jamovat covery Nirvany. Oba jsme do toho byli patřičně zapálení. Postupem času však začalo být zkoušení u Filipa problém, jelikož naše grungové vyhrávání rušilo jeho rodiče, a tak jsme se vydali hledat zkušebnu. No a také jsme ji našli, i když to byla asi trochu jiná zkušebna, než si většina z vás představí. Začali jsme totiž zkoušet u mě na vesnici v hospodském sále. Na každou zkoušku jsme tak museli tahat celé elektrické bicí z mého pokoje, Filipovo kombo, mikrofon, reproduktor a tak dále. Když se na toto období zpětně podívám, vlastně ani nechápu, jak jsme to mohli zvládat, ale v té době hold nebyla jiná možnost.
                        </p><p>
                        Za možnost zkoušení na sále jsme byli vděční, avšak časem nás už nebavilo nosit pořád všechny věci, a tak jsme začali hledat novou zkušebnu. K našemu vlastnímu překvapení jsme ji našli a ještě k tomu v centru Turnova! Byl to pro nás oba velký pokrok. Nyní tomu bylo asi dva roky, co jsme spolu hráli a s novou zkušebnou jsem si také splnil sen pořízením akustických bicích. Začali jsme však také cítit, že ve dvou lidech to již nebude ono, a tak jsme dali inzerát na basáka. Trvalo to asi dva měsíce a basák se nám opravdu přihlásil. Jmenoval se Honza Kudrnáč a s ním jsme se začali konečně pouštět do vlastní tvorby. Když si občas zpětně poslechnu nějakou nahrávku bez basy, tak mě udivuje, že nám to trvalo dva roky.
                        </p><p>
                        S basou a vlastní tvorbou začalo přicházet i více koncertů. Hodně silný zážitek byl pro nás koncert před Visacím zámkem. Překvapilo nás, jak lidi tancovali, tleskali a jaká skvělá zpětná vazba tam celkově byla. Z pódia to byl prostě úžasný pohled. Vzniklo tam i naše první foto s fanoušky. Tento koncert byl jeden z prvních impulsů, který nastartoval naše další hraní. Hodně akcí jsme hráli hlavně s kapelou Ovčie Kiahně a Dědkové. Dnes už máme zas o něco širší síť kontaktů. Jako naši hlavní příležitost teď vidíme spolupráci s kapelou Tři sestry.
                        </p><p>
                        S Honzou jsme si užili spoustu srandy, avšak v říjnu 2022 přišel čas loučení. Honza je nyní šťastný otec malé dcery a na kapelu bohužel nezbýval čas, a tak se do kapely přidal Šimon Světlík. Se Šimonem jsem se znal už dlouhou dobu. Hráli jsme spolu v big bandu ZUŠ a tak jsem věděl, koho do kapely přibíráme. Jak už to bývá, nový člen se projevil novým druhem skládání, vlastním pohledem na věci a posunul naši tvorbu lehce jiným směrem. Dost těžko se mi tento posun dává do slov. Z mého pohledu se tohle slovy popisuje obtížně, ale s Filipem jsme oba cítili, že songy, které nově tvoříme, mají nový vibe.
                        </p><p>
                        Zábavné bylo, že když se Šimon učil od Honzy basové party, tak Honzovi bylo 26 a Šimonovi 14. Ode mě a od Filipa je Šimon o 4 roky mladší. I přesto jsme si všichni sedli dobře a věkový rozdíl který mezi námi je vlastně ani tolik nevnímáme. Možná to je tím, že Šimon není vyloženě typický zástupce své generace. Tak jako tak jsme si každý pobrali jeden od druhého nemálo z hudby i z kamarádského života.
                        <p></p>
                        V této sestavě jsme zůstali až do května 2024. Máme totiž nejčerstvější přírůstek, a tím je druhý kytarista Ondřej Havlíček. Ondra nás po delší době, stejně jako dříve Šimon, pěkně nakopnul. Jeho hlavní sílou jsou šíleně zamotaná a rychlá sóla, ze kterých jsme pořád všichni tak trochu na větvi.
                        </p><p>
                        No a tak máme dnešní plnou sestavu! Možná si říkáš, jak to dnes může vypadat, když tvoříme nové songy. Řekl bych, že každá kapela to má trochu jinak. U nás začíná vše s hlavní iniciativou Filipa. Většinou vychází z riffů na kytaru, když utvoří hlavní melodii s různými riffy, udělá i základ ostatních nástrojů. Na zkoušku nám pak přinese nahrávku, kterou si poslechneme a postupně začínáme song přehrávat. Každý si svůj part následně upraví podle svého. Někdy donese Filip jen riffy na kytaru, pak tvoříme vše na místě. Text vzniká v obou případech až na posledním místě.
                        </p><p>
                        Květen byl pro nás opravdu nabitý. Vydali jsme totiž 1. vlastní album s názvem Under The Spectrum. Abychom mohli vůbec album nahrát potřebovali jsme digitální mixovací pult, mikrofony, kabely atd… Na všechno toto vybavení jsme si naštěstí našetřily z koncertů, které jsme za ty roky odehráli. Vtipné  bylo, že nahrávat jsme album začali již kolem Ledna a mysleli jsme si, že v Únoru bude připravené na vydání, to se trochu nepovedlo, jelikož mixování zabralo mnohem více času, než jsme čekali.  Na druhou stranu jsme tím získali mnoho cenných zkušeností a při křtu alba jsme se dmuli pýchou. 
                        </p><p>
                        Tak to bylo něco o nás díky za přečtení a těšíme se a tebe na dalším koncertě!

                        </p>
